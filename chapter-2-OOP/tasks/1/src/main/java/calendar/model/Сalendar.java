package main.java.calendar.model;


public class Сalendar {
    private int year;
    private int month;

    public Сalendar(int year, int month) {
        this.year = year;
        this.month = month;
    }

    public int getYear() {
        return this.year;
    }

    public int getMonth() {
        return this.month;
    }
}


